#WHILE LOOPING (PERULANGAN)

angka = 0
# print(f"Angka sekarang --> {angka}")

while angka < 10:
    angka +=1   # <-- Disaat nilai (0) lebih kecil daripada nilai (10) maka akan true dan di tambah satu
    print(f"Angka sekarang --> {angka}")

print("Akhir baris program..")  # <-- Jika nilai var (angka) sudah lebih besar dari (10) maka false dan masuk kesini